ALTER TABLE course MODIFY category VARCHAR(50);

UPDATE course SET category = 'OTHER'
WHERE category NOT IN (
                       'TECHNOLOGY','BUSINESS','DESIGN','PROGRAMMING','DATA_SCIENCE',
                       'HEALTH','MUSIC','ART','EDUCATION','LANGUAGE','SCIENCE','MATH',
                       'HISTORY','FINANCE','TRAVEL','SPORTS','PSYCHOLOGY','MARKETING','GAMING','OTHER'
    ) OR category IS NULL;

